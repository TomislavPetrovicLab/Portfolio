# Madlibs game
# word game where you create a story
# by filling in blanks with random words

noun1 = input("Input a noun (person, name, animal): ")
adjective1 = input("Input an adjective (description): ")
adjective2 = input("Input another adjective (emotional): ")
adjective3 = input("Input another adjective (emotional): ")
verb1 = input("Input a verb ending with -ing: ")

print(f"Today I went to a {adjective1} zoo.")
print(f"I saw {noun1} in the cage.")
print(f"{noun1} was {adjective2} and {verb1}")
print(f"I was {adjective3}!")